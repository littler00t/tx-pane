# ---------------------------------------------------------------------
# Two run modes: inside the container (real Linux tools) or on the host.
# Inside the container, $TX_IN_DOCKER=1 (set by the Dockerfile).
# ---------------------------------------------------------------------

def _in_container() -> bool:
    import os
    return os.environ.get("TX_IN_DOCKER") == "1"


def _run_local(shell: str, *, timeout: float = 20) -> str | None:
    """Run a shell snippet locally; return stdout (+ stderr if non-zero exit)."""
    try:
        res = subprocess.run(
            shell, shell=True, capture_output=True, text=True,
            timeout=timeout, executable="/bin/bash",
        )
        return res.stdout + (res.stderr if res.returncode != 0 else "")
    except (subprocess.TimeoutExpired, OSError):
        return None


def _useful(out: str | None, *, min_nonblank_lines: int = 5) -> bool:
    """Heuristic: is this output worth using as the 'raw' sample?

    To *demonstrate* a normalizer we need at least a few lines of
    realistic content. Trivial errors (`Cannot open ...`, `Permission
    denied`, `No such file or directory`, `System has not been booted`,
    a header-only response with no data rows) don't show the normalizer
    doing meaningful work — fall back to a captured Linux-server
    fixture instead.

    Rejection criteria (any → fall back):
    - None
    - `command not found`
    - any of the known "tool installed but environment can't produce
      real output" markers (no PID 1 systemd, no /dev/sda, no daemon,
      etc.)
    - fewer than ``min_nonblank_lines`` non-blank lines
    """
    if out is None:
        return False
    s = out.strip()
    if not s:
        return False
    if "command not found" in out:
        return False
    REJECT_MARKERS = (
        "No such file or directory",
        "Permission denied (you must be root)",
        "Failed to connect to the hypervisor",
        "Cannot connect to the Docker daemon",
        "System has not been booted with systemd",
        "Failed to read messages from /dev/kmsg",
        "Operation not permitted",
        "No journal files were found",
        "modprobe: FATAL",
        "ZFS modules are not loaded",
        "no pools available",
        "Smartctl open device",
        "Unable to detect device type",
        "/dev/sda: failed to open",
        "/dev/sda: No such",
        "Cannot stat device",
    )
    for m in REJECT_MARKERS:
        if m in out and len(out) < 800:
            return False
    nonblank = sum(1 for l in out.splitlines() if l.strip())
    return nonblank >= min_nonblank_lines


def _resolve(fx: dict) -> tuple[str, str, str]:
    """Return (raw_text, source_label, mode) for this fixture.

    mode is 'real' or 'captured'.

    A fixture may set ``prefer_captured = True`` when the container's
    real output won't demonstrate the normalizer (e.g. `find` as root
    has no perm-denied lines to strip; `apt list --upgradable` on a
    fresh image is empty). Those still ship with a clean captured
    fixture sourced from a real Linux server.
    """
    real = None
    if _in_container() and not fx.get("prefer_captured"):
        if fx.get("setup"):
            _run_local(fx["setup"], timeout=15)
            time.sleep(0.1)
        real = _run_local(fx["shell_cmd"], timeout=20)
    if _useful(real):
        return (real, fx.get("source_when_real",
                              "real (Linux container)"), "real")
    captured = fx.get("captured")
    if captured:
        return (captured, fx.get("source_when_captured",
                                 "captured Linux output"), "captured")
    return (
        f"(no output: container produced nothing useful for `{fx['shell_cmd']}` "
        f"and no captured fixture was set)",
        "no-output",
        "captured",
    )


