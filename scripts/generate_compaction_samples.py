def fence(text: str) -> str:
    return f"```\n{text.rstrip()}\n```"


def render() -> str:
    out: list[str] = []
    out.append("# `tx` Compaction Samples")
    out.append("")
    out.append("Auto-generated by `scripts/generate_compaction_samples.py`.")
    out.append("Before/after pairs for every builtin normalizer in `tx_compact/`.")
    out.append("")
    if _in_container():
        out.append("**Generated inside the tx-tests Docker container** "
                   "(Debian bookworm). Most samples are real `tx run` output "
                   "captured against the installed Linux binaries; the rest "
                   "are captured fixtures from Linux servers where the "
                   "container can't run the tool (no PID 1 systemd, no ZFS "
                   "kernel module, no libvirtd, etc.). Source is labelled "
                   "per section.")
    else:
        out.append("**Generated on the host (NOT in the Docker container).** "
                   "This means `_run_local()` ran each command on the dev "
                   "machine (likely macOS). Most rows will fall back to "
                   "captured fixtures. To regenerate from real Linux output, "
                   "run: `make samples` or directly: "
                   "`docker run --rm -v $PWD:/work --entrypoint "
                   "/work/scripts/generate_compaction_samples.py "
                   "tx-tests:latest > docs/compaction_samples.md`")
    out.append("")
    out.append("Each section shows:")
    out.append("- The shell command an agent would issue.")
    out.append("- **Raw** — what `tx run --raw <pane> '<cmd>'` returns "
               "(cleaned bytes verbatim).")
    out.append("- **Compacted** — what `tx run --terse <pane> '<cmd>'` returns.")
    out.append("- Byte-savings + which compaction layers fired.")
    out.append("")
    out.append("**If a normalizer dropped information you need**, re-run "
               "the single command with `--no-normalize` (keep L1-L5) or "
               "`--raw` (escape hatch). See `docs/tx-doc-compaction.md` "
               "→ \"When compaction strips info you need\".")
    out.append("")

    total_in = 0
    total_out = 0
    rows: list[tuple[str, int, int, float, str, str, str]] = []

    for fx in FIXTURES:
        raw, source, mode = _resolve(fx)
        ctx = CompactCtx(
            mode="terse",
            cmd=fx["shell_cmd"],
            pane="p1",
            run_id="r-sample",
            token_budget=None,
        )
        result = compact(raw, ctx)
        out.append(f"## {fx['title']} — `{fx['name']}` ({mode})")
        out.append("")
        out.append(f"**Source:** {source}")
        out.append("")
        out.append(f"**Command:** `{fx['shell_cmd']}`")
        out.append("")
        out.append(f"**Raw ({len(raw):,} bytes):**")
        out.append("")
        out.append(fence(raw))
        out.append("")
        out.append(f"**Compacted ({len(result.text):,} bytes, "
                   f"saved {result.saved_pct:.1f}%, "
                   f"tier={result.tier.name.lower()}):**")
        out.append("")
        out.append(fence(result.text))
        out.append("")
        layers = ", ".join(result.applied_layers) if result.applied_layers else "(none)"
        out.append(f"**Layers fired:** {layers}")
        out.append("")
        if result.notes:
            out.append("**Notes:**")
            out.append("")
            for note in result.notes:
                out.append(f"- {note}")
            out.append("")
        out.append("---")
        out.append("")

        total_in += len(raw)
        total_out += len(result.text)
        rows.append((fx["name"], len(raw), len(result.text), result.saved_pct,
                     layers, result.tier.name.lower(), mode))

    # Headline summary table inserted after the intro.
    summary: list[str] = []
    summary.append("## Headline savings")
    summary.append("")
    overall = (100.0 * (total_in - total_out) / total_in) if total_in else 0.0
    summary.append(f"Across the {len(rows)} normalizers below: "
                   f"**{total_in:,} bytes → {total_out:,} bytes "
                   f"({overall:.1f}% saved)**.")
    summary.append("")
    summary.append("| Normalizer | Source | Raw B | Compacted B | Saved | Tier | Layers |")
    summary.append("|---|---|---:|---:|---:|---|---|")
    for name, ib, ob, pct, layers, tier, mode in rows:
        summary.append(f"| `{name}` | {mode} | {ib:,} | {ob:,} | "
                       f"{pct:.1f}% | {tier} | {layers} |")
    summary.append("")
    summary.append("`source` = `real` (ran inside the Docker container, "
                   "Debian + the sysadmin toolbox) or `captured` "
                   "(snapshot from a Linux server where the unprivileged "
                   "container can't run the tool).")
    summary.append("")
    summary.append("---")
    summary.append("")
    # Insert summary right after the intro paragraphs (before any section).
    insert_at = next(i for i, line in enumerate(out) if line.startswith("## "))
    return "\n".join(out[:insert_at] + summary + out[insert_at:])


# ---------------------------------------------------------------------
# Self-relaunch into Docker if not already inside
# ---------------------------------------------------------------------

def _docker_available() -> bool:
    return shutil.which("docker") is not None


def _relaunch_in_container(image: str, rebuild: bool) -> int:
    """Build (if needed) the test image and re-run this script inside.
    Streams the rendered markdown back to stdout."""
    if rebuild:
        print(f"[samples] docker build --no-cache -t {image} ...",
              file=sys.stderr)
        rc = subprocess.call(
            ["docker", "build", "--no-cache", "-t", image, str(REPO)],
        )
        if rc != 0:
            return rc
    else:
        # Always do a (cached) build to pick up code changes.
        print(f"[samples] docker build -t {image} ...", file=sys.stderr)
        rc = subprocess.call(["docker", "build", "-t", image, str(REPO)])
        if rc != 0:
            return rc

    print(f"[samples] docker run inside {image} ...", file=sys.stderr)
    proc = subprocess.run(
        [
            "docker", "run", "--rm",
            "-v", f"{REPO}:/work",
            "--entrypoint", "/work/scripts/generate_compaction_samples.py",
            image,
        ],
        capture_output=True, text=True,
    )
    # Forward stderr (informational) and emit stdout (the markdown).
    sys.stderr.write(proc.stderr)
    sys.stdout.write(proc.stdout)
    return proc.returncode


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--image", default="tx-tests:latest")
    parser.add_argument("--rebuild", action="store_true")
    parser.add_argument("--host", action="store_true",
                        help="Skip the docker relaunch and render from the host "
                             "(samples will mostly fall back to captured fixtures).")
    args = parser.parse_args()

    if _in_container() or args.host:
        sys.stdout.write(render())
        return 0
    if not _docker_available():
        print("[samples] docker not available; rendering on host (most "
              "samples will be captured fixtures). Pass --host to silence.",
              file=sys.stderr)
        sys.stdout.write(render())
        return 0
    return _relaunch_in_container(args.image, args.rebuild)


if __name__ == "__main__":
    sys.exit(main())
