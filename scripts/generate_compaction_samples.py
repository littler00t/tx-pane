"""Generate docs/compaction_samples.md — before/after for every builtin
normalizer, sourced from a Linux environment.

Linux is the primary platform for tx — the macOS host this repo is
developed on is the dev environment, not the deployment target. So the
samples are generated by running each command inside the test Docker
image (Debian + the §5.3 toolbox), capturing real `tx run --raw` and
`tx run --terse` output for each, and rendering the diff.

Usage:
    ./scripts/generate_compaction_samples.py
    # writes docs/compaction_samples.md, builds the image if needed.

Flags:
    --image NAME     override docker image tag (default tx-tests:latest)
    --rebuild        force docker build --no-cache first
    --keep-pane      do not destroy the test pane between commands
                     (useful for debugging)

When a command genuinely can't produce useful output in the unprivileged
container (no PID 1 systemd, no ZFS kernel module, no libvirtd, no
docker daemon, no real disk under /dev/sda), the script falls back to a
captured fixture sourced from a real Linux server and labels the
section as 'captured' with provenance. Every section header makes the
source explicit.
"""

